// Function to translate a specification string (Chinese to English)
export function translateSpec(spec: string): string {
  let result = spec;

  // Sort translations by length (longest first) to avoid partial replacements
  const sortedTranslations = Object.entries(specTranslations)
    .sort((a, b) => b[0].length - a[0].length);

  for (const [chinese, english] of sortedTranslations) {
    result = result.replace(chinese, english);
  }

  return result;
}

// Function to reverse translate a specification string (English to Chinese)
export function translateSpecReverse(spec: string): string {
  let result = spec;

  // Sort translations by length (longest first) to avoid partial replacements
  const sortedTranslations = Object.entries(specTranslationsReverse)
    .sort((a, b) => b[0].length - a[0].length);

  for (const [english, chinese] of sortedTranslations) {
    result = result.replace(english, chinese);
  }

  return result;
}

// Function to translate all specs in a product (Chinese to English)
export function translateProductSpecs(specs: Record<string, string | undefined>): Record<string, string | undefined> {
  const translatedSpecs: Record<string, string | undefined> = {};

  for (const [key, value] of Object.entries(specs)) {
    if (value) {
      translatedSpecs[key] = translateSpec(value);
    }
  }

  return translatedSpecs;
}

// Function to reverse translate all specs in a product (English to Chinese)
export function translateProductSpecsReverse(specs: Record<string, string | undefined>): Record<string, string | undefined> {
  const translatedSpecs: Record<string, string | undefined> = {};

  for (const [key, value] of Object.entries(specs)) {
    if (value) {
      translatedSpecs[key] = translateSpecReverse(value);
    }
  }

  return translatedSpecs;
}